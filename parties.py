# -*- coding: utf-8 -*-
"""
parties.py — 政黨正規化與分組（全專案共用）

目的：把歷年（2014/2018/2022）議員與政黨票資料中，名稱不一致、改名、簡寫的
政黨字串，統一成穩定的代碼，並標記哪些屬於「小黨／第三勢力」與使用者鎖定的
三個焦點政黨（時代力量、台灣基進、歐巴桑聯盟）。
"""
from __future__ import annotations

# --- 名稱別名 → 正規化全名（處理改名/簡寫/異體字）---
ALIASES = {
    "歐巴桑聯盟": "小民參政歐巴桑聯盟",
    "小民參政歐巴桑聯盟": "小民參政歐巴桑聯盟",
    "基進黨": "台灣基進",          # 台灣基進前身
    "台灣基進": "台灣基進",
    "綠黨": "綠黨",                # 2014/2018 多稱「綠黨」
    "台灣綠黨": "綠黨",            # 後期申報名
    "綠黨社會民主黨聯盟": "綠黨社會民主黨聯盟",
    "無黨籍及未經政黨推薦": "無黨籍",
    "無黨籍": "無黨籍",
    "中國國民黨": "中國國民黨",
    "民主進步黨": "民主進步黨",
    "台灣民眾黨": "台灣民眾黨",
}


def norm_party(p: str | None) -> str:
    p = (p or "").strip().replace("臺", "台")
    return ALIASES.get(p, p or "無黨籍")


# --- 焦點政黨：可在地圖「焦點政黨」排單獨檢視熱區的小黨 ---
# 時代力量、台灣基進、小民參政歐巴桑聯盟、綠黨、社會民主黨。
FOCUS = {"時代力量", "台灣基進", "小民參政歐巴桑聯盟", "綠黨", "社會民主黨"}

# 焦點黨的短碼（圖層/欄位用）
FOCUS_CODE = {
    "時代力量": "npp",
    "台灣基進": "tsp",
    "小民參政歐巴桑聯盟": "obasan",
    "綠黨": "green",
    "社會民主黨": "sdp",
}

# --- 小黨 = 公民進步第三勢力（焦點黨 + 同一波進步側小黨）---
# 設計取捨：聚焦 2014 太陽花後興起的公民/進步第三勢力，這才是使用者要的訊號。
#   納入：時力、基進、歐巴桑、綠黨、社民黨、樹黨、人民民主、左翼聯盟、綠社盟。
#   *不*納入老牌小黨（台灣團結聯盟、親民黨、新黨、無黨團結聯盟…）— 性質不同，
#   會稀釋第三勢力訊號。若要改變定義，只需調整此集合。
SMALL_PARTIES = FOCUS | {
    "綠黨社會民主黨聯盟",
    "樹黨",
    "人民民主黨",
    "人民民主陣線",
    "左翼聯盟",
}

# 老牌/其他小黨：另成一類，預設不計入「小黨合計」，但保留以便切換比較。
LEGACY_MINOR = {"台灣團結聯盟", "親民黨", "新黨", "無黨團結聯盟", "勞動黨"}

# --- 主要政黨分組（競爭格局圖層用配色）---
MAJOR = {"中國國民黨", "民主進步黨", "台灣民眾黨"}


def party_group(p: str) -> str:
    """回傳圖層分組：kmt / dpp / tpp / small / focus / independent / other。"""
    p = norm_party(p)
    if p in FOCUS:
        return "focus"
    if p == "中國國民黨":
        return "kmt"
    if p == "民主進步黨":
        return "dpp"
    if p == "台灣民眾黨":
        return "tpp"
    if p in SMALL_PARTIES:
        return "small"
    if p == "無黨籍":
        return "independent"
    return "other"


def is_small(p: str) -> bool:
    return norm_party(p) in SMALL_PARTIES


def is_focus(p: str) -> bool:
    return norm_party(p) in FOCUS
